// Helper function to get random car
export function getRandomCar() {
  const brands = Object.keys(carData);
  const randomBrand = brands[Math.floor(Math.random() * brands.length)];
  const models = carData[randomBrand];
  const randomModel = models[Math.floor(Math.random() * models.length)];

  return {
    brand: randomBrand,
    model: randomModel,
    year: Math.floor(Math.random() * (2024 - 2010) + 2010),
    mileage: Math.floor(Math.random() * 200000),
    startingPrice: Math.floor(Math.random() * (50000 - 5000) + 5000),
    description: `Хорошо ухоженный ${randomBrand} ${randomModel} ${Math.floor(
      Math.random() * (2024 - 2010) + 2010,
    )} года выпуска. Автомобиль регулярно обслуживается и находится в отличном состоянии.`,
  };
}
